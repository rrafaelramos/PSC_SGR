    @Override
    public void carregaObjeto() throws ViolacaoRegraNegocioException {
        paraSalvar.setNome( txtNome.getText() );
        paraSalvar.setCpf( txtCpf.getText() );
        paraSalvar.setObs(txtObs.getText() );
    }

    @Override
    public void setCampos(HashMap<String, String> filtro) {
        for(String key : filtro.keySet()){
            if(key=="nome")
                txtNome.setText(filtro.get(key));
            if(key=="cpf")
                txtCpf.setText(filtro.get(key));    
        }
    }

    @Override
    public void setCampos(Cliente filtro) {
        txtNome.setText(filtro.getNome());
        txtCpf.setText(filtro.getCpf());
        txtObs.setText(filtro.getObs());
        paraSalvar.setId(filtro.getId());
        paraSalvar.setEndereco(filtro.getEndereco());
    }
    
