    @Override
    public void carregaObjeto() throws ViolacaoRegraNegocioException {
        paraSalvar.setNome( txtNome.getText() );
        paraSalvar.setUserName(txtUserName.getText() );
        if( String.valueOf(txtPassword.getPassword()) == null ? String.valueOf(txtConfirmacao.getPassword()) != null : !String.valueOf(txtPassword.getPassword()).equals(String.valueOf(txtConfirmacao.getPassword())) )
            throw new ViolacaoRegraNegocioException("Confirmação diferente de senha");
        paraSalvar.setPassword(String.valueOf(txtPassword.getPassword()));
    }

    @Override
    public void setCampos(HashMap<String, String> filtro) {
        for(String key : filtro.keySet()){
            if(key=="nome")
                txtNome.setText(filtro.get(key));
            if(key=="userName")
                txtUserName.setText(filtro.get(key));
            if(key=="password")
                txtPassword.setText(filtro.get(key));
            if(key=="id")
                paraSalvar.setId(new Integer(filtro.get(key))); 
        }
    }

    @Override
    public void setCampos(Usuario filtro) {
        txtNome.setText(filtro.getNome());
        txtUserName.setText(filtro.getUserName());
        txtPassword.setText(filtro.getPassword());
        paraSalvar.setId(filtro.getId());
    }

