    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            filtroEmailPreDeterminado.clear();
            filtroEmailPreDeterminado.put("individuo", Integer.toString(paraSalvar.getId()));
            if(Repositorios.getEmailRepositorio().Buscar(filtroEmailPreDeterminado).isEmpty())
                salvar(false);
            else
                salvar(true);
        } catch (ViolacaoRegraNegocioException ex) {
            Logger.getLogger(UsuarioEditar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEmailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmailActionPerformed
        if(paraSalvar.getId()==0)
            JOptionPane.showMessageDialog(rootPane, "Cadastre Nome , Login e Senha primeiro.");
        else{
            filtroEmailPreDeterminado.clear();
            filtroEmailPreDeterminado.put("individuo", Integer.toString(paraSalvar.getId()));

            EmailBuscar tela = new EmailBuscar(Repositorios.getEmailRepositorio(), EmailEditar.class, filtroEmailPreDeterminado);

            tela.buscar();

            this.getParent().add(tela);

            tela.setTelaMae(this);

            tela.centraliza();

            tela.setVisible(true);
        }
    }//GEN-LAST:event_btnEmailActionPerformed

